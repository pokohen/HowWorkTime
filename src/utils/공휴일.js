/**
 * 연도별 공휴일 Set 반환
 * @param {number} 연도
 * @returns {Set<string>}
 */
export function 연도별공휴일조회(연도) {
  return new Set(공휴일목록[연도] || [])
}

/**
 * 특정 월의 공휴일 목록 반환
 * @param {number} 연도
 * @param {number} 월 - 1~12
 * @returns {string[]}
 */
export function 월별공휴일조회(연도, 월) {
  const 전체공휴일 = 공휴일목록[연도] || []
  const 날짜접두사 = `${연도}-${String(월).padStart(2, '0')}`
  return 전체공휴일.filter((항목) => 항목.startsWith(날짜접두사))
}

